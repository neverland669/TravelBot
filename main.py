def handle_location(message) -> None:
    user_location = message.location
    latitude = user_location.latitude
    longitude = user_location.longitude

    # –ó–∞–ø—Ä–æ—Å –∫ Google Places API
    url = f'https://search-maps.yandex.ru/v1/?apikey={YANDEX_MAPS_API_KEY}&text=–¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏&lang=ru_RU&ll={longitude},{latitude}&spn=0.1,0.1&type=biz'
    response = requests.get(url)
    data = response.json()

    if 'features' in data:
        attractions = data['features']
        if attractions:
            message_t = "–ë–ª–∏–∂–∞–π—à–∏–µ –¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏:\n"
            for attraction in attractions[:5]:  # –û–≥—Ä–∞–Ω–∏—á–∏–º—Å—è –ø–µ—Ä–≤—ã–º–∏ 5 –¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—è–º–∏
                name = attraction['properties']['name']
                address = attraction['properties'].get('description', '–ê–¥—Ä–µ—Å –Ω–µ —É–∫–∞–∑–∞–Ω')
                message_t += f"- {name} ({address})\n"
        else:
            message_t = "–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, —Ä—è–¥–æ–º —Å –≤–∞–º–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–µ–π."
    else:
        message_t = "–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–∏—Å–∫–µ –¥–æ—Å—Ç–æ–ø—Ä–∏–º–µ—á–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–µ–π."
        logger.error(f"Yandex Maps API error: {data.get('message', 'Unknown error')}")

    bot.send_message(message.chat.id, message_t)
    message.text = '–°–ø–∏—Å–æ–∫ –≥–æ—Ä–æ–¥–æ–≤'
    on_click(message)


